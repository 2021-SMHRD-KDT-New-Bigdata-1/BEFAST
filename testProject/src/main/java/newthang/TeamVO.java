package newthang;

public class TeamVO {
	private String team_name;
	private int team_code; 
	private String team_field;
	private String team_level;
	private String team_uniform;
	private String team_info;
	private String team_member;
	private String team_time;
	
	public String getTeam_name() {
		return team_name;
	}
	public void setTeam_name(String team_name) {
		this.team_name = team_name;
	}
	public int getTeam_code() {
		return team_code;
	}
	public void setTeam_code(int team_code) {
		this.team_code = team_code;
	}
	public String getTeam_field() {
		return team_field;
	}
	public void setTeam_field(String team_field) {
		this.team_field = team_field;
	}
	public String getTeam_level() {
		return team_level;
	}
	public void setTeam_level(String team_level) {
		this.team_level = team_level;
	}
	public String getTeam_uniform() {
		return team_uniform;
	}
	public void setTeam_uniform(String team_uniform) {
		this.team_uniform = team_uniform;
	}
	public String getTeam_info() {
		return team_info;
	}
	public void setTeam_info(String team_info) {
		this.team_info = team_info;
	}
	public String getTeam_member() {
		return team_member;
	}
	public void setTeam_member(String team_member) {
		this.team_member = team_member;
	}
	public String getTeam_time() {
		return team_time;
	}
	public void setTeam_time(String team_time) {
		this.team_time = team_time;
	}
	
	
	
	
}
